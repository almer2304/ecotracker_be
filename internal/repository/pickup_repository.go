type PickupRepository struct {
	db *pgxpool.Pool
}

func NewPickupRepository(db *pgxpool.Pool) *PickupRepository {
	return &PickupRepository{db: db}
}

// scanPickup scans a full pickup row
func scanPickup(row pgx.Row) (*domain.Pickup, error) {
	p := &domain.Pickup{}
	err := row.Scan(
		&p.ID, &p.UserID, &p.CollectorID, &p.Status,
		&p.Address, &p.Latitude, &p.Longitude,
		&p.PhotoURL, &p.Notes, &p.CompletedAt, &p.CreatedAt,
	)
	if err != nil {
		if errors.Is(err, pgx.ErrNoRows) {
			return nil, domain.ErrNotFound
		}
		return nil, fmt.Errorf("scan pickup: %w", err)
	}
	return p, nil
}

// Create inserts a new pickup (UUID generated by DB)
func (r *PickupRepository) Create(ctx context.Context, p *domain.Pickup) error {
	query := `
		INSERT INTO pickups (id, user_id, status, address, latitude, longitude, photo_url, notes, created_at)
		VALUES (gen_random_uuid(), $1, 'pending', $2, $3, $4, $5, $6, NOW() AT TIME ZONE 'Asia/Jakarta')
		RETURNING id, COALESCE(collector_id::text,''), status, created_at`

	err := r.db.QueryRow(ctx, query,
		p.UserID, p.Address, p.Latitude, p.Longitude, p.PhotoURL, p.Notes,
	).Scan(&p.ID, &p.CollectorID, &p.Status, &p.CreatedAt)
	if err != nil {
		return fmt.Errorf("create pickup: %w", err)
	}
	return nil
}

// GetByID fetches a single pickup by its UUID
func (r *PickupRepository) GetByID(ctx context.Context, id string) (*domain.Pickup, error) {
	query := `
		SELECT id, user_id, COALESCE(collector_id::text,''), status,
		       address, latitude, longitude,
		       COALESCE(photo_url,''), COALESCE(notes,''), completed_at, created_at
		FROM pickups WHERE id=$1`

	return scanPickup(r.db.QueryRow(ctx, query, id))
}


func (r *PickupRepository) ListByStatusWithPagination(
	ctx context.Context,
	status string,
	limit int,
	offset int,
) ([]domain.Pickup, int, error) {
	var totalCount int
	countQuery := `SELECT COUNT(*) FROM pickups WHERE status = $1`
	err := r.db.QueryRow(ctx, countQuery, status).Scan(&totalCount)
	if err != nil {
		return nil, 0, fmt.Errorf("count: %w", err)
	}

	query := `
		SELECT id, user_id, COALESCE(collector_id::text,''), status,
		       address, latitude, longitude,
		       COALESCE(photo_url,''), COALESCE(notes,''), completed_at, ted_at
		FROM pickups 
		WHERE status = $1 
		ORDER BY created_at DESC
		LIMIT $2 OFFSET $3`

	rows, err := r.db.Query(ctx, query, status, limit, offset)
	if err != nil {
		return nil, 0, fmt.Errorf("query: %w", err)
	}
	defer rows.Close()

	pickups, err := scanPickupRows(rows)
	return pickups, totalCount, err
}